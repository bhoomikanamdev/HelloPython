import numpy as np

my_array=np.array([1,2,3,4])
print(my_array)

my_array_2D=np.array([[1,2],[2,2]])
print(my_array_2D)

my_array_3D=np.array([[[1,2],[2,2],[3,3]]])
print(my_array_3D)

empty_arr=np.empty((3,2))
print(empty_arr)

zeros_arr=np.zeros((3,2))
print(zeros_arr)

my_array_2D2=np.array([[1.5,2],[2,2]], dtype=float)
print(my_array_2D2)
